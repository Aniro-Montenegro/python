# Adição (+):
print(2 + 2)
# Subtração (-):
print(2 - 2)
# Multiplicação (*):
print(2 * 2)
# Divisão (/):
print(2 / 2)
# Divisão inteira (//):
print(2 // 2)
# Resto da divisão (%):
print(2 % 2)
# Potência (**):
print(2 ** 2)
# Ordem de precedência:
print(5 + 2 * 10)
print((5 + 2) * 10)
print(5 + 2 * 10 ** 2)
print((5 + 2) * 10 ** 2)
# Concatenação de strings:
print('5' + '2')
